"""Summator module."""


def summator(fst: int, snd: int) -> int:
    """
    Get the sum of two values.

    Args:
        fst: int - first value
        snd: int - second value

    Returns:
        int - result of summator
    """
    return fst + snd  # это Илья который слишком длинный для ванильного флейка только сейчас эта строка длинная


if __name__ == '__main__':
    print(summator(1, 8))
